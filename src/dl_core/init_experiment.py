"""Create a new standalone experiment repository scaffold."""

from __future__ import annotations

import argparse
import re
import shutil
from pathlib import Path


def _slugify(value: str) -> str:
    """Convert text to a filesystem-friendly slug."""
    normalized = re.sub(r"[^a-zA-Z0-9]+", "-", value.strip()).strip("-").lower()
    if not normalized:
        raise ValueError("Name must contain at least one alphanumeric character")
    return normalized


def _to_package_name(value: str) -> str:
    """Convert text to a Python package name."""
    slug = _slugify(value)
    package_name = slug.replace("-", "_")
    if package_name[0].isdigit():
        package_name = f"exp_{package_name}"
    return package_name


def _to_class_name(value: str) -> str:
    """Convert text to a PascalCase class-friendly name."""
    parts = re.split(r"[^a-zA-Z0-9]+|_", value)
    return "".join(part.capitalize() for part in parts if part)


def _templates_dir() -> Path:
    """Return the bundled templates directory."""
    return Path(__file__).resolve().parent / "templates"


def _copy_template(src: Path, dest: Path) -> None:
    """Copy a template file to destination."""
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dest)


def _write_text(path: Path, content: str) -> None:
    """Write text content to a file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _replace_template_tokens(path: Path, replacements: dict[str, str]) -> None:
    """Replace placeholder values inside a text file."""
    content = path.read_text(encoding="utf-8")
    for old, new in replacements.items():
        content = content.replace(old, new)
    path.write_text(content, encoding="utf-8")


def _project_pyproject(project_name: str, package_name: str, with_azure: bool) -> str:
    """Render the generated experiment project's pyproject.toml."""
    dependencies = ['    "dl-core>=0.1.3",']
    if with_azure:
        dependencies.append('    "dl-mobai-azure>=0.1.0",')

    deps_block = "\n".join(dependencies)
    return f"""[build-system]
requires = ["hatchling>=1.25.0"]
build-backend = "hatchling.build"

[project]
name = "{project_name}"
version = "0.1.0"
description = "Experiment repository for {project_name}."
readme = "README.md"
requires-python = ">=3.10"
dependencies = [
{deps_block}
]

[tool.hatch.build.targets.wheel]
packages = ["src/{package_name}"]
"""


def _project_readme(project_name: str, package_name: str, with_azure: bool) -> str:
    """Render the generated experiment README."""
    azure_note = (
        "\nThis scaffold includes `dl-mobai-azure` as a dependency.\n"
        if with_azure
        else "\nAzure support can be added later by installing `dl-mobai-azure`.\n"
    )
    return f"""# {project_name}

Experiment repository generated by `dl-init-experiment`.

## Layout

- `configs/`: base config, presets, and sweeps
- `src/{package_name}/models/`: local model extensions
- `src/{package_name}/datasets/`: local dataset extensions
- `src/{package_name}/trainers/`: local trainer extensions

## Recommended workflow

1. Install this repository in editable mode.
2. Update `configs/base.yaml` for your dataset and run naming.
3. Run `dl-sweep --sweep configs/sweeps/example_sweep.yaml` for a first dry run.
4. The scaffold defaults to thin project-named wrappers for trainer, model, and
   dataset so local customization stays isolated from `dl-core`.
{azure_note}
"""


def _root_presets() -> str:
    """Render repository-level presets placeholder file."""
    return """# Repository-Level Presets
#
# Use this file for presets shared across multiple configs or sweeps in this
# experiment repository.
#
# Example:
# default_runtime:
#   runtime.log_level: INFO
"""


def _config_presets() -> str:
    """Render config-level presets placeholder file."""
    return """# Config-Level Presets
#
# Use this file for experiment-specific presets referenced by sweeps.
#
# Example:
# fast_debug:
#   trainer.standard.epochs: 3
"""


def _gitignore() -> str:
    """Render the scaffold .gitignore."""
    return """.venv/
__pycache__/
*.pyc
.pytest_cache/
artifacts/
dist/
build/
*.egg-info/
"""


def _package_init(package_name: str, with_azure: bool) -> str:
    """Render the top-level experiment package __init__ file."""
    azure_import = ""
    if with_azure:
        azure_import = "import dl_mobai_azure  # noqa: F401\n\n"

    return (
        f'"""Local extensions for {package_name}."""\n\n'
        f"{azure_import}"
        "from . import datasets, models, trainers\n\n"
        '__all__ = ["datasets", "models", "trainers"]\n'
    )


def _datasets_init(module_name: str, class_name: str) -> str:
    """Render the local datasets package __init__ file."""
    return (
        '"""Local dataset extensions."""\n\n'
        f"from .{module_name} import {class_name}\n\n"
        f'__all__ = ["{class_name}"]\n'
    )


def _models_init(module_name: str, class_name: str) -> str:
    """Render the local models package __init__ file."""
    return (
        '"""Local model extensions."""\n\n'
        f"from .{module_name} import {class_name}\n\n"
        f'__all__ = ["{class_name}"]\n'
    )


def _trainers_init(module_name: str, class_name: str) -> str:
    """Render the local trainers package __init__ file."""
    return (
        '"""Local trainer extensions."""\n\n'
        f"from .{module_name} import {class_name}\n\n"
        f'__all__ = ["{class_name}"]\n'
    )


def _project_standard_dataset(dataset_name: str, class_name: str) -> str:
    """Render a project-specific dataset example."""
    return f"""\"\"\"Example project dataset wrapper.\"\"\"

from dl_core.core import register_dataset
from dl_core.datasets.standard import StandardWrapper


@register_dataset("{dataset_name}")
class {class_name}(StandardWrapper):
    \"\"\"Thin local wrapper around the built-in standard dataset.\"\"\"
"""


def _project_resnet_model(model_name: str, class_name: str) -> str:
    """Render a project-specific model example."""
    return f"""\"\"\"Example project ResNet wrapper.\"\"\"

from dl_core.core import register_model
from dl_core.models.resnet import ResNet


@register_model("{model_name}")
class {class_name}(ResNet):
    \"\"\"Thin local wrapper around the built-in ResNet model.\"\"\"
"""


def _project_standard_trainer(trainer_name: str, class_name: str) -> str:
    """Render a project-specific trainer example."""
    return f"""\"\"\"Example project trainer wrapper.\"\"\"

from dl_core.core import register_trainer
from dl_core.trainers.standard_trainer import StandardTrainer


@register_trainer("{trainer_name}")
class {class_name}(StandardTrainer):
    \"\"\"Thin local wrapper around the built-in standard trainer.\"\"\"
"""


def _resolve_project_name(name: str | None, root_dir: Path) -> str:
    """Resolve the project name from explicit input or the target directory."""
    if name is not None:
        return name

    project_name = root_dir.name
    if not project_name:
        raise ValueError("Could not infer a project name from the target directory")
    return project_name


def _resolve_target_dir(name: str | None, root_dir: Path) -> Path:
    """Resolve the scaffold target directory."""
    if name is None:
        return root_dir
    return root_dir / _slugify(name)


_IN_PLACE_ALLOWED_ENTRIES = {
    ".git",
    ".gitignore",
    ".python-version",
    ".venv",
    "README.md",
    "main.py",
    "pyproject.toml",
    "uv.lock",
}

_IN_PLACE_REPLACED_FILES = {
    ".gitignore",
    "README.md",
    "pyproject.toml",
}

_IN_PLACE_REMOVED_FILES = {
    "main.py",
    "uv.lock",
}


def _cleanup_in_place_target_dir(target_dir: Path) -> None:
    """Remove transient bootstrap files before writing the scaffold."""
    for relative_name in _IN_PLACE_REPLACED_FILES | _IN_PLACE_REMOVED_FILES:
        path = target_dir / relative_name
        if path.exists() and path.is_file():
            path.unlink()


def _validate_target_dir(target_dir: Path, initialize_in_place: bool) -> None:
    """Validate that the scaffold target directory is safe to create."""
    if target_dir.exists() and not target_dir.is_dir():
        raise FileExistsError(f"Target path is not a directory: {target_dir}")

    if not target_dir.exists():
        return

    if not initialize_in_place:
        raise FileExistsError(f"Target directory already exists: {target_dir}")

    existing_names = {path.name for path in target_dir.iterdir()}
    if not existing_names:
        return

    unsupported_entries = sorted(existing_names - _IN_PLACE_ALLOWED_ENTRIES)
    if unsupported_entries:
        raise FileExistsError(
            "Target directory already exists and contains unsupported files: "
            f"{unsupported_entries}. Directory: {target_dir}"
        )


def create_experiment_scaffold(
    name: str | None = None,
    root_dir: str = ".",
    package_name: str | None = None,
    with_azure: bool = False,
) -> Path:
    """Create a new experiment repository scaffold."""
    root_path = Path(root_dir).resolve()
    project_name = _resolve_project_name(name, root_path)
    project_slug = _slugify(project_name)
    package_name = package_name or _to_package_name(project_name)
    dataset_name = package_name
    trainer_name = package_name
    dataset_class_name = f"{_to_class_name(package_name)}Dataset"
    model_name = "resnet_example"
    model_class_name = "ResNetExample"
    trainer_class_name = f"{_to_class_name(package_name)}Trainer"
    target_dir = _resolve_target_dir(name, root_path)
    _validate_target_dir(target_dir, initialize_in_place=name is None)
    if name is None and target_dir.exists():
        _cleanup_in_place_target_dir(target_dir)

    templates_dir = _templates_dir()

    (target_dir / "configs" / "sweeps").mkdir(parents=True, exist_ok=True)
    (target_dir / "src" / package_name / "datasets").mkdir(parents=True, exist_ok=True)
    (target_dir / "src" / package_name / "models").mkdir(parents=True, exist_ok=True)
    (target_dir / "src" / package_name / "trainers").mkdir(parents=True, exist_ok=True)

    _copy_template(templates_dir / "base" / "base.yaml", target_dir / "configs" / "base.yaml")
    _copy_template(
        templates_dir / "base" / "base_sweep.yaml",
        target_dir / "configs" / "base_sweep.yaml",
    )
    _copy_template(
        templates_dir / "sweeps" / "example_sweep.yaml",
        target_dir / "configs" / "sweeps" / "example_sweep.yaml",
    )

    replacements = {
        "my_run": f"{project_slug}_baseline",
        "my_experiment": project_slug,
        "models:\n  resnet:\n    name: resnet": (
            "models:\n"
            f"  {model_name}:\n"
            f"    name: {model_name}"
        ),
        "dataset:\n  name: standard": (
            "dataset:\n"
            f"  name: {dataset_name}"
        ),
        "trainer:\n  standard:\n    name: standard": (
            "trainer:\n"
            f"  {trainer_name}:\n"
            f"    name: {trainer_name}"
        ),
        "  trainer:\n    standard:\n      name: standard": (
            "  trainer:\n"
            f"    {trainer_name}:\n"
            f"      name: {trainer_name}"
        ),
    }
    _replace_template_tokens(target_dir / "configs" / "base.yaml", replacements)
    _replace_template_tokens(target_dir / "configs" / "base_sweep.yaml", replacements)

    _write_text(target_dir / "presets.yaml", _root_presets())
    _write_text(target_dir / "configs" / "presets.yaml", _config_presets())
    _write_text(
        target_dir / "pyproject.toml",
        _project_pyproject(project_slug, package_name, with_azure),
    )
    _write_text(
        target_dir / "README.md",
        _project_readme(project_slug, package_name, with_azure),
    )
    _write_text(target_dir / ".gitignore", _gitignore())
    _write_text(
        target_dir / "src" / package_name / "__init__.py",
        _package_init(package_name, with_azure),
    )
    dataset_module_name = dataset_name
    _write_text(
        target_dir / "src" / package_name / "datasets" / "__init__.py",
        _datasets_init(dataset_module_name, dataset_class_name),
    )
    _write_text(
        target_dir / "src" / package_name / "datasets" / f"{dataset_module_name}.py",
        _project_standard_dataset(dataset_name, dataset_class_name),
    )
    _write_text(
        target_dir / "src" / package_name / "models" / "__init__.py",
        _models_init(model_name, model_class_name),
    )
    _write_text(
        target_dir / "src" / package_name / "models" / f"{model_name}.py",
        _project_resnet_model(model_name, model_class_name),
    )
    trainer_module_name = trainer_name
    _write_text(
        target_dir / "src" / package_name / "trainers" / "__init__.py",
        _trainers_init(trainer_module_name, trainer_class_name),
    )
    _write_text(
        target_dir / "src" / package_name / "trainers" / f"{trainer_module_name}.py",
        _project_standard_trainer(trainer_name, trainer_class_name),
    )

    return target_dir


def main(argv: list[str] | None = None) -> int:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description="Initialize a standalone experiment repository scaffold.",
    )
    parser.add_argument(
        "--name",
        help=(
            "Optional experiment repository name. If omitted, dl-init-experiment "
            "uses the target directory name and initializes that directory in place."
        ),
    )
    parser.add_argument(
        "--root-dir",
        default=".",
        help=(
            "Target directory for the scaffold. If --name is provided, a new "
            "slugified subdirectory is created under this path. If --name is "
            "omitted, this directory itself is initialized."
        ),
    )
    parser.add_argument(
        "--package-name",
        help="Optional Python package name for local experiment extensions.",
    )
    parser.add_argument(
        "--with-azure",
        action="store_true",
        help="Include dl-mobai-azure as a dependency in the generated project.",
    )
    args = parser.parse_args(argv)

    target_dir = create_experiment_scaffold(
        name=args.name,
        root_dir=args.root_dir,
        package_name=args.package_name,
        with_azure=args.with_azure,
    )
    print(f"Created experiment scaffold: {target_dir}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

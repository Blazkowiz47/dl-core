def _resolve_project_name(name: str | None, root_dir: Path) -> str:
    """Resolve the project name from explicit input or the target directory."""
    if name is not None:
        return name

    project_name = root_dir.name
    if not project_name:
        raise ValueError("Could not infer a project name from the target directory")
    return project_name


def _resolve_target_dir(name: str | None, root_dir: Path) -> Path:
    """Resolve the scaffold target directory."""
    if name is None:
        return root_dir
    return root_dir / _slugify(name)


def _build_project_names(project_name: str) -> ProjectNames:
    """Build normalized scaffold naming metadata for a project."""
    component_name = _to_package_name(project_name)
    return ProjectNames(
        project_name=project_name,
        project_slug=_slugify(project_name),
        component_name=component_name,
        dataset_name=component_name,
        dataset_class_name=f"{_to_class_name(component_name)}Dataset",
        model_name="resnet_example",
        model_class_name="ResNetExample",
        trainer_name=component_name,
        trainer_class_name=f"{_to_class_name(component_name)}Trainer",
    )


def _base_scaffold_files(
    templates_dir: Path,
    project: ProjectNames,
    preserved_uv_blocks: str = "",
) -> dict[Path, str]:
    """Render the base scaffold files before extensions are applied."""
    replacements = {
        "my_run": f"{project.project_slug}_baseline",
        "my_experiment": project.project_slug,
        "models:\n  resnet:\n    name: resnet": (
            "models:\n"
            f"  {project.model_name}:\n"
            f"    name: {project.model_name}"
        ),
        "dataset:\n  name: standard": (
            "dataset:\n"
            f"  name: {project.dataset_name}"
        ),
        "trainer:\n  standard:\n    name: standard": (
            "trainer:\n"
            f"  {project.trainer_name}:\n"
            f"    name: {project.trainer_name}"
        ),
        "  trainer:\n    standard:\n      name: standard": (
            "  trainer:\n"
            f"    {project.trainer_name}:\n"
            f"      name: {project.trainer_name}"
        ),
    }
    base_yaml = _replace_template_tokens(
        _template_text(templates_dir / "base" / "base.yaml"),
        replacements,
    )
    base_sweep_yaml = _replace_template_tokens(
        _template_text(templates_dir / "base" / "base_sweep.yaml"),
        replacements,
    )
    lr_sweep_yaml = _template_text(templates_dir / "sweeps" / "example_sweep.yaml")
    dataset_module_name = project.dataset_name
    trainer_module_name = project.trainer_name
    return {
        Path("configs") / "base.yaml": base_yaml,
        Path("configs") / "base_sweep.yaml": base_sweep_yaml,
        Path("configs") / "presets.yaml": _config_presets(),
        Path("experiments") / "lr_sweep.yaml": lr_sweep_yaml,
        Path("experiments") / "experiments.log": _experiments_log(),
        Path("pyproject.toml"): _project_pyproject(
            project.project_slug,
            preserved_uv_blocks=preserved_uv_blocks,
        ),
        Path("README.md"): _project_readme(project.project_slug),
        Path("AGENTS.md"): _project_agents_md(project.project_slug),
        Path(".gitignore"): _gitignore(),
        Path("pyrightconfig.json"): _pyright_config(),
        Path("src") / "bootstrap.py": _bootstrap_module(),
        Path("src") / "datasets" / "__init__.py": _datasets_init(
            dataset_module_name,
            [project.dataset_class_name],
        ),
        Path("src") / "datasets" / f"{dataset_module_name}.py": (
            _project_dataset_skeleton(
                project.dataset_name,
                project.dataset_class_name,
            )
        ),
        Path("src") / "models" / "__init__.py": _models_init(
            project.model_name,
            project.model_class_name,
        ),
        Path("src") / "models" / f"{project.model_name}.py": _project_resnet_model(
            project.model_name,
            project.model_class_name,
        ),
        Path("src") / "trainers" / "__init__.py": _trainers_init(
            trainer_module_name,
            project.trainer_class_name,
        ),
        Path("src") / "trainers" / f"{trainer_module_name}.py": (
            _project_standard_trainer(
                project.trainer_name,
                project.trainer_class_name,
            )
        ),
    }


_IN_PLACE_ALLOWED_ENTRIES = {
    ".git",
    ".gitignore",
    ".python-version",
    "AGENTS.md",
    ".venv",
    "README.md",
    "pyrightconfig.json",
    "azure-config.json",
    "scripts",
    "main.py",
    "pyproject.toml",
    "uv.lock",
}

_IN_PLACE_REPLACED_FILES = {
    ".gitignore",
    "AGENTS.md",
    "README.md",
    "pyrightconfig.json",
    "pyproject.toml",
}

_IN_PLACE_REMOVED_FILES = {
    "main.py",
    "uv.lock",
}


def _cleanup_in_place_target_dir(target_dir: Path) -> None:
    """Remove transient bootstrap files before writing the scaffold."""
    for relative_name in _IN_PLACE_REPLACED_FILES | _IN_PLACE_REMOVED_FILES:
        path = target_dir / relative_name
        if path.exists() and path.is_file():
            path.unlink()


def _validate_target_dir(target_dir: Path, initialize_in_place: bool) -> None:
    """Validate that the scaffold target directory is safe to create."""
    if target_dir.exists() and not target_dir.is_dir():
        raise FileExistsError(f"Target path is not a directory: {target_dir}")

    if not target_dir.exists():
        return

    if not initialize_in_place:
        raise FileExistsError(f"Target directory already exists: {target_dir}")

    existing_names = {path.name for path in target_dir.iterdir()}
    if not existing_names:
        return

    unsupported_entries = sorted(existing_names - _IN_PLACE_ALLOWED_ENTRIES)
    if unsupported_entries:
        raise FileExistsError(
            "Target directory already exists and contains unsupported files: "
            f"{unsupported_entries}. Directory: {target_dir}"
        )


def create_experiment_scaffold(
    name: str | None = None,
    root_dir: str = ".",
    with_azure: bool = False,
    enabled_extensions: set[str] | None = None,
    discovered_extensions: dict[str, InitExtension] | None = None,
) -> Path:
    """Create a new experiment repository scaffold."""
    root_path = Path(root_dir).resolve()
    project_name = _resolve_project_name(name, root_path)
    project = _build_project_names(project_name)
    target_dir = _resolve_target_dir(name, root_path)
    _validate_target_dir(target_dir, initialize_in_place=name is None)
    preserved_uv_blocks = ""
    existing_pyproject = target_dir / "pyproject.toml"
    if name is None and existing_pyproject.exists():
        preserved_uv_blocks = _extract_tool_uv_blocks(
            existing_pyproject.read_text(encoding="utf-8")
        )
    if name is None and target_dir.exists():
        _cleanup_in_place_target_dir(target_dir)

    templates_dir = _templates_dir()
    extensions = (
        discovered_extensions
        if discovered_extensions is not None
        else discover_init_extensions()
    )
    selected_extensions = set(enabled_extensions or set())
    if with_azure:
        selected_extensions.add("azure")

    context = ScaffoldContext(
        target_dir=target_dir,
        templates_dir=templates_dir,
        project=project,
        files=_base_scaffold_files(
            templates_dir,
            project,
            preserved_uv_blocks=preserved_uv_blocks,
        ),
        enabled_extensions=set(selected_extensions),
    )
    for extension_name in sorted(selected_extensions):
        extension = extensions.get(extension_name)
        if extension is None:
            raise ValueError(f"Unknown init extension: {extension_name}")
        extension.apply(context)

    for relative_path, content in context.files.items():
        _write_text(target_dir / relative_path, content)

    return target_dir


def main(argv: list[str] | None = None) -> int:
    """CLI entry point."""
    discovered_extensions = discover_init_extensions()
    parser = argparse.ArgumentParser(
        description="Initialize a standalone experiment repository scaffold.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Typical workflow:\n"
            "  dl-init-experiment --name my-exp --root-dir .\n"
            "  cd my-exp\n"
            "  uv sync\n"
            "  edit src/datasets/my_exp.py and configs/base.yaml\n"
            "  uv run dl-run --config configs/base.yaml\n\n"
            "If you omit --name, the target directory itself is initialized."
        ),
    )
    parser.add_argument(
        "--name",
        help=(
            "Optional experiment repository name. If omitted, dl-init-experiment "
            "uses the target directory name and initializes that directory in place."
        ),
    )
    parser.add_argument(
        "--root-dir",
        default=".",
        help=(
            "Target directory for the scaffold. If --name is provided, a new "
            "slugified subdirectory is created under this path. If --name is "
            "omitted, this directory itself is initialized."
        ),
    )
    for extension in discovered_extensions.values():
        extension.add_arguments(parser)
    args = parser.parse_args(argv)

    target_dir = create_experiment_scaffold(
        name=args.name,
        root_dir=args.root_dir,
        with_azure=bool(getattr(args, "with_azure", False)),
        enabled_extensions=resolve_enabled_extensions(args, discovered_extensions),
        discovered_extensions=discovered_extensions,
    )
    project_name = _resolve_project_name(args.name, target_dir)
    project = _build_project_names(project_name)
    print(f"Created experiment scaffold: {target_dir}")
    print("Next steps:")
    print("  1. Run `uv sync` inside the generated repository.")
    print(
        "  2. Read `AGENTS.md` and implement "
        f"`src/datasets/{project.dataset_name}.py`."
    )
    print("  3. Adjust `configs/base.yaml`.")
    print("  4. Start with `uv run dl-run --config configs/base.yaml`.")
    print("  5. Then try `uv run dl-sweep --sweep experiments/lr_sweep.yaml`.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

def _slugify(value: str) -> str:
    """Convert text to a filesystem-friendly slug."""
    normalized = re.sub(r"[^a-zA-Z0-9]+", "-", value.strip()).strip("-").lower()
    if not normalized:
        raise ValueError("Name must contain at least one alphanumeric character")
    return normalized


def _to_package_name(value: str) -> str:
    """Convert text to a Python package name."""
    slug = _slugify(value)
    package_name = slug.replace("-", "_")
    if package_name[0].isdigit():
        package_name = f"exp_{package_name}"
    return package_name


def _to_class_name(value: str) -> str:
    """Convert text to a PascalCase class-friendly name."""
    parts = re.split(r"[^a-zA-Z0-9]+|_", value)
    return "".join(part.capitalize() for part in parts if part)


def _templates_dir() -> Path:
    """Return the bundled templates directory."""
    return Path(__file__).resolve().parent / "templates"


def _write_text(path: Path, content: str) -> None:
    """Write text content to a file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _replace_template_tokens(content: str, replacements: dict[str, str]) -> str:
    """Replace placeholder values inside a text blob."""
    for old, new in replacements.items():
        content = content.replace(old, new)
    return content


def _template_text(path: Path) -> str:
    """Read a template file as text."""
    return path.read_text(encoding="utf-8")


def _extract_tool_uv_blocks(pyproject_text: str) -> str:
    """Extract raw ``tool.uv`` TOML blocks from an existing pyproject."""
    lines = pyproject_text.splitlines()
    captured_blocks: list[str] = []
    current_block: list[str] = []
    capturing = False

    for line in lines:
        stripped = line.strip()
        is_header = stripped.startswith("[") and stripped.endswith("]")
        if is_header:
            if capturing and current_block:
                captured_blocks.append("\n".join(current_block).rstrip())
                current_block = []
            capturing = stripped.startswith("[tool.uv") or stripped.startswith(
                "[[tool.uv"
            )
        if capturing:
            current_block.append(line)

    if capturing and current_block:
        captured_blocks.append("\n".join(current_block).rstrip())

    if not captured_blocks:
        return ""
    return "\n\n".join(captured_blocks) + "\n"


def _project_pyproject(project_name: str, preserved_uv_blocks: str = "") -> str:
    """Render the generated experiment project's pyproject.toml."""
    content = f"""[build-system]
requires = ["hatchling>=1.25.0"]
build-backend = "hatchling.build"

[project]
name = "{project_name}"
version = "0.1.0"
description = "Experiment repository for {project_name}."
readme = "README.md"
requires-python = ">=3.10"
dependencies = [
    "deep-learning-core",
]

[tool.hatch.build.targets.wheel]
only-include = ["src"]
sources = ["src"]
"""
    if preserved_uv_blocks:
        content = f"{content.rstrip()}\n\n{preserved_uv_blocks}"
    return content


def _project_readme(project_name: str) -> str:
    """Render the generated experiment README."""
    project_component = _to_package_name(project_name)
    return f"""# {project_name}

Experiment repository generated by `dl-init-experiment`.

## Layout

- `configs/`: base config, base sweep template, and presets
- `experiments/`: concrete sweep definitions
- `src/models/`: local model extensions
- `src/datasets/`: local dataset extensions
- `src/trainers/`: local trainer extensions
- `src/bootstrap.py`: optional project-level imports loaded before local components
- `AGENTS.md`: repository-specific guidance for humans and coding agents
- `pyrightconfig.json`: basic static-analysis settings for the local repo

## Recommended workflow

1. Install this repository in editable mode.
2. Open these files first:
   - `src/datasets/{project_component}.py`
   - `configs/base.yaml`
   - `experiments/lr_sweep.yaml`
   - `AGENTS.md`
3. Replace the generated dataset wrapper with your real dataset logic.
4. Update `configs/base.yaml` for your dataset path and run naming.
5. Start with `uv run dl-run --config configs/base.yaml`.
6. Move on to `uv run dl-sweep --sweep experiments/lr_sweep.yaml` once a
   single run works.

The scaffold defaults to thin project-named wrappers for trainer, model, and
dataset so local customization stays isolated from `deep-learning-core`.

Extensions can be added later by installing the relevant
`deep-learning-core[...]`
extras and re-running the scaffold in a new repository.
"""



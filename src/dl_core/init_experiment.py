def _resolve_project_name(name: str | None, root_dir: Path) -> str:
    """Resolve the project name from explicit input or the target directory."""
    if name is not None:
        return name

    project_name = root_dir.name
    if not project_name:
        raise ValueError("Could not infer a project name from the target directory")
    return project_name


def _resolve_target_dir(name: str | None, root_dir: Path) -> Path:
    """Resolve the scaffold target directory."""
    if name is None:
        return root_dir
    return root_dir / _slugify(name)


def _build_project_names(project_name: str) -> ProjectNames:
    """Build normalized scaffold naming metadata for a project."""
    component_name = _to_package_name(project_name)
    return ProjectNames(
        project_name=project_name,
        project_slug=_slugify(project_name),
        component_name=component_name,
        dataset_name=component_name,
        dataset_class_name=f"{_to_class_name(component_name)}Dataset",
        model_name="resnet_example",
        model_class_name="ResNetExample",
        trainer_name=component_name,
        trainer_class_name=f"{_to_class_name(component_name)}Trainer",
    )



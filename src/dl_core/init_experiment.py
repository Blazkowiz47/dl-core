def _temporary_dataset_script() -> str:
    """Render the scaffolded dataset smoke helper."""
    return '''#!/usr/bin/env python3
"""Smoke-check the dataset wrapper configured in configs/base.yaml."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

import torch

from dl_core import load_builtin_components, load_local_components
from dl_core.core import DATASET_REGISTRY
from dl_core.utils.config import load_config


def _force_local_loader_settings(dataset_config: dict[str, Any]) -> dict[str, Any]:
    """Force single-process loader settings for a quick local smoke check."""
    config = dict(dataset_config)
    config["num_workers"] = {"train": 0, "validation": 0, "test": 0}
    config["prefetch_factor"] = {
        "train": None,
        "validation": None,
        "test": None,
    }
    return config


def _format_value(value: Any) -> str:
    """Format a sample or batch value for readable terminal output."""
    if isinstance(value, torch.Tensor):
        return f"tensor(shape={list(value.shape)}, dtype={value.dtype})"
    if isinstance(value, dict):
        return f"dict(keys={sorted(value.keys())})"
    if isinstance(value, list):
        return f"list(len={len(value)})"
    return repr(value)


def _print_mapping(title: str, values: dict[str, Any]) -> None:
    """Print a flat mapping with formatted values."""
    print(title)
    for key, value in values.items():
        print(f"  - {key}: {_format_value(value)}")


def _build_parser() -> argparse.ArgumentParser:
    """Create the CLI parser for the dataset smoke helper."""
    parser = argparse.ArgumentParser(
        description="Inspect one dataset sample and one collated batch.",
    )
    parser.add_argument(
        "--config",
        default="configs/base.yaml",
        help="Path to the experiment config file.",
    )
    parser.add_argument(
        "--split",
        default="train",
        choices=["train", "validation", "test"],
        help="Dataset split to inspect.",
    )
    parser.add_argument(
        "--index",
        type=int,
        default=0,
        help="Sample index to inspect inside the chosen split.",
    )
    return parser


def main() -> int:
    """Load the configured dataset wrapper and print quick debug summaries."""
    args = _build_parser().parse_args()
    config_path = Path(args.config).resolve()

    load_builtin_components()
    load_local_components(str(config_path))

    config = load_config(str(config_path))
    dataset_config = _force_local_loader_settings(config["dataset"])
    dataset_wrapper = DATASET_REGISTRY.get(
        dataset_config["name"],
        dataset_config,
    )
    data_loader = dataset_wrapper.get_split(args.split)

    if data_loader is None:
        raise ValueError(f"No data loader available for split: {args.split}")

    dataset = data_loader.dataset
    if args.index < 0 or args.index >= len(dataset):
        raise IndexError(
            f"Sample index {args.index} is out of range for split {args.split}"
        )

    print(f"Config: {config_path}")
    print(f"Dataset wrapper: {dataset_wrapper.__class__.__name__}")
    print(f"Split: {args.split}")
    print(f"Dataset length: {len(dataset)}")
    print(f"Num batches: {len(data_loader)}")

    split_stats = dataset_wrapper.get_stats(args.split)
    if split_stats:
        print("Split stats:")
        for stat in split_stats:
            print(f"  - {stat}")

    sample = dataset[args.index]
    if isinstance(sample, dict):
        _print_mapping("Sample summary:", sample)
    else:
        print(f"Sample summary: {_format_value(sample)}")

    batch = next(iter(data_loader))
    if isinstance(batch, dict):
        _print_mapping("Batch summary:", batch)
    else:
        print(f"Batch summary: {_format_value(batch)}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


def _temporary_model_script() -> str:
    """Render the scaffolded model smoke helper."""
    return '''#!/usr/bin/env python3
"""Smoke-check the configured model with a dummy forward pass."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

import torch

from dl_core import load_builtin_components, load_local_components
from dl_core.core import MODEL_REGISTRY
from dl_core.utils.config import load_config


def _build_parser() -> argparse.ArgumentParser:
    """Create the CLI parser for the model smoke helper."""
    parser = argparse.ArgumentParser(
        description="Instantiate one configured model and run a dummy forward pass.",
    )
    parser.add_argument(
        "--config",
        default="configs/base.yaml",
        help="Path to the experiment config file.",
    )
    parser.add_argument(
        "--model-key",
        default=None,
        help="Specific key from the models section to instantiate.",
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=2,
        help="Dummy batch size for the forward pass.",
    )
    return parser


def _resolve_model_entry(
    config: dict[str, Any],
    requested_key: str | None,
) -> tuple[str, dict[str, Any]]:
    """Resolve one model entry from the config."""
    models_config = config.get("models", {})
    if not isinstance(models_config, dict) or not models_config:
        raise ValueError("Config must define at least one model under models")

    if requested_key is None:
        model_key = next(iter(models_config))
    elif requested_key in models_config:
        model_key = requested_key
    else:
        available = ", ".join(models_config.keys())
        raise KeyError(
            f"Unknown model key '{requested_key}'. Available keys: {available}"
        )

    model_config = dict(models_config[model_key])
    model_config.setdefault("num_classes", config["dataset"].get("num_classes", 2))
    return model_key, model_config


def _build_dummy_batch(
    config: dict[str, Any],
    batch_size: int,
) -> dict[str, torch.Tensor]:
    """Create a default image batch from the dataset config."""
    dataset_config = config.get("dataset", {})
    height = int(dataset_config.get("height", 64))
    width = int(dataset_config.get("width", 64))
    return {
        "image": torch.zeros(batch_size, 3, height, width, dtype=torch.float32),
    }


def _format_output(value: Any) -> str:
    """Format one model output value for terminal output."""
    if isinstance(value, torch.Tensor):
        return f"tensor(shape={list(value.shape)}, dtype={value.dtype})"
    return repr(value)


def main() -> int:
    """Load the configured model and run one dummy forward pass."""
    args = _build_parser().parse_args()
    config_path = Path(args.config).resolve()

    load_builtin_components()
    load_local_components(str(config_path))

    config = load_config(str(config_path))
    model_key, model_config = _resolve_model_entry(config, args.model_key)
    model = MODEL_REGISTRY.get(model_config["name"], model_config)
    model.eval()

    batch = _build_dummy_batch(config, args.batch_size)
    with torch.no_grad():
        outputs = model(batch)

    model_info = model.get_model_info()
    print(f"Config: {config_path}")
    print(f"Model key: {model_key}")
    print(f"Model class: {model.__class__.__name__}")
    print(f"Num parameters: {model_info['num_parameters']}")
    print(f"Num trainable parameters: {model_info['num_trainable_parameters']}")
    print("Output summary:")
    for key, value in outputs.items():
        print(f"  - {key}: {_format_output(value)}")
    print(
        "Note: if your model expects non-image batch keys, edit "
        "scripts/temporary/test_model.py locally."
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


